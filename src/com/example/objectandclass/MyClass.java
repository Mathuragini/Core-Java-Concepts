package com.example.objectandclass;

//Defining a class
public class MyClass {

	//defining attributes
	int id;
	String name;
	
	 public static void main(String args[]){  
		  //Creating an object or instance  
		 MyClass myclass =new MyClass();//creating an object of Student  
		 
		  //Printing values of the object  
		  System.out.println(myclass.id); //accessing member through reference variable  
		  System.out.println(myclass.name);  
		 }  
	
}
